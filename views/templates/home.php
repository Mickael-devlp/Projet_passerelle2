                <h2 class="h2_presentation">Pourquoi ce site ?</h2>
                    <p class="text_presentation">
                        Ce site est né de ma passion profonde pour le Middle-Earth Strategy Battle Game et pour l’univers merveilleux de la Terre du Milieu. 
                        J’ai grandi avec les films de Tolkien, et j’ai lu tous ses livres, me laissant emporter par ses histoires et ses batailles épiques. 
                        J’avais envie de créer un endroit où cette passion pourrait se partager pleinement : un espace regroupant scénarios, vidéos, tacticas et conseils, 
                        pour que chaque joueur, débutant ou confirmé, puisse y trouver de l’inspiration et des ressources.

                        Au-delà du jeu, ce site est avant tout un lieu de rencontre et d’échange entre passionnés. 
                        Mon but est de transmettre l’enthousiasme et l’émerveillement que suscitent les histoires de Tolkien et les combats stratégiques du jeu, 
                        tout en donnant à chacun les clés pour vivre ses propres aventures. Ici, chaque figurine, chaque scénario et chaque conseil racontent une histoire, 
                        et j’espère que ce site saura éveiller la curiosité et la créativité de tous les fans.
                    </p>

                <h2 class="h2_presentation">Présentation de l’histoire du jeu MESBG</h2>
                    <p class="text_presentation">
                        Le Middle-Earth Strategy Battle Game (MESBG) est un jeu de figurines tactique basé sur l’univers riche et détaillé de J.R.R. Tolkien, 
                        couvrant principalement les événements des films Le Seigneur des Anneaux et Le Hobbit et du dernier de la saga La Guerre des Rohirims. Le jeu a été développé 
                        par Games Workshop et permet aux joueurs 
                        de recréer les grandes batailles de la Terre du Milieu, en incarnant les armées de la Communauté de l’Anneau, des royaumes humains et elfes, ou celles 
                        des forces du mal comme les Orques, les Gobelins et les Nazgûls.

                        L’histoire du MESBG suit "fidèlement" les conflits majeurs de Tolkien, depuis l’invasion de la Moria et les escarmouches dans la Comté, jusqu’aux guerres 
                        titanesques du Gouffre de Helm et de Minas Tirith. Chaque bataille est conçue pour refléter les événements clés des romans et surtout des films, avec une attention 
                        particulière aux détails des armements, des unités et des héros. Les joueurs peuvent ainsi revivre les aventures de personnages emblématiques comme 
                        Gandalf, Aragorn, Legolas ou encore Thorin et ses compagnons, en reproduisant leurs exploits sur le champ de bataille.

                        Au-delà de la simple reconstitution, MESBG offre un cadre narratif immersif : chaque scénario propose des objectifs spécifiques et des règles spéciales, 
                        permettant aux joueurs de revivre les stratégies et les défis propres à chaque confrontation. Les batailles ne sont pas seulement des affrontements de 
                        figurines, mais deviennent de véritables récits tactiques où la planification et la stratégie sont essentielles.

                        Ainsi, MESBG permet aux fans de Tolkien et aux amateurs de jeux de figurines de plonger au cœur de la Terre du Milieu, de prendre part à ses grandes 
                        guerres et d’écrire leurs propres histoires au fil des batailles. Le jeu est à la fois un hommage fidèle aux œuvres originales et une expérience 
                        stratégique passionnante pour tous les passionnés de fantasy.
                    </p>
