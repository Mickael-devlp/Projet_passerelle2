                <h2 class="h2_presentation">Fédération Française de la Terre du Milieu (FFTM)</h2>
                    <p class="text_presentation">
                        La FFTM est une association française regroupant les passionnés de l’univers de J.R.R. Tolkien 
                        et des jeux de figurines inspirés de la Terre du Milieu, notamment le Middle-Earth Strategy Battle Game (MESBG). 
                        Elle a pour objectif de promouvoir le jeu, d’organiser des tournois, des événements et des rencontres entre joueurs, et de créer un réseau communautaire 
                        autour des batailles épiques de la Terre du Milieu.

                        La fédération fournit également des ressources tel qu'une application "MAT", qui permet de créer ses listes d'armées, des conseils à travers ce lien discord et 
                        un cadre officiel pour les compétitions, permettant aux joueurs de tous niveaux 
                        de participer et de développer leurs compétences stratégiques. Grâce à la FFTM, les fans peuvent partager leur passion, échanger sur les scénarios, 
                        les figurines et les stratégies, et perpétuer l’héritage de Tolkien à travers le jeu.
                    </p>
                    <p class="right_link">
